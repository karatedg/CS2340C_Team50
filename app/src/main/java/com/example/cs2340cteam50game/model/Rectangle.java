package com.example.cs2340cteam50game.model;

public class Rectangle {
    public float left;
    public float top;
    public float right;
    public float bottom;

    public Rectangle(float left, float top, float right, float bottom) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }
}
